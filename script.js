const header = document.querySelector("[data-header]");
const navToggle = document.querySelector("[data-nav-toggle]");
const navMenu = document.querySelector("[data-nav-menu]");
const navLinks = document.querySelectorAll("[data-nav-menu] a");
const revealTargets = document.querySelectorAll(".reveal");
const yearTarget = document.querySelector("[data-year]");
const languageButtons = document.querySelectorAll("[data-language]");
const defaultLanguage = "en";

function updateHeaderState() {
  if (!header) return;
  header.classList.toggle("is-scrolled", window.scrollY > 12);
}

function closeMenu() {
  if (!navToggle || !navMenu) return;
  navToggle.setAttribute("aria-expanded", "false");
  navMenu.classList.remove("is-open");
  document.body.classList.remove("menu-open");
}

function setupNavigation() {
  if (!navToggle || !navMenu) return;

  navToggle.addEventListener("click", () => {
    const isOpen = navToggle.getAttribute("aria-expanded") === "true";
    navToggle.setAttribute("aria-expanded", String(!isOpen));
    navMenu.classList.toggle("is-open", !isOpen);
    document.body.classList.toggle("menu-open", !isOpen);
  });

  navLinks.forEach((link) => {
    link.addEventListener("click", closeMenu);
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") closeMenu();
  });
}

function setupRevealAnimations() {
  if (!("IntersectionObserver" in window)) {
    revealTargets.forEach((target) => target.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      });
    },
    { threshold: 0.14 }
  );

  revealTargets.forEach((target) => observer.observe(target));
}

function setupActiveNavigation() {
  const sectionLinks = [...navLinks].filter((link) => {
    const href = link.getAttribute("href");
    return href && href.startsWith("#");
  });

  const sections = sectionLinks
    .map((link) => document.querySelector(link.getAttribute("href")))
    .filter(Boolean);

  if (!sections.length || !("IntersectionObserver" in window)) return;

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        const activeLink = document.querySelector(`[data-nav-menu] a[href="#${entry.target.id}"]`);
        sectionLinks.forEach((link) => link.classList.toggle("is-active", link === activeLink));
      });
    },
    {
      rootMargin: "-38% 0px -56% 0px",
      threshold: 0,
    }
  );

  sections.forEach((section) => observer.observe(section));
}

function getStoredLanguage() {
  try {
    return localStorage.getItem("obai4ai-language");
  } catch (error) {
    return null;
  }
}

function saveLanguage(language) {
  try {
    localStorage.setItem("obai4ai-language", language);
  } catch (error) {
    return;
  }
}

function getTranslation(language, key) {
  return translations[language]?.[key] || translations[defaultLanguage][key] || "";
}

function updateMetadata(language) {
  const meta = translations[language]?.meta || translations[defaultLanguage].meta;
  const description = document.querySelector('meta[name="description"]');
  const ogTitle = document.querySelector('meta[property="og:title"]');
  const ogDescription = document.querySelector('meta[property="og:description"]');
  const ogLocale = document.querySelector('meta[property="og:locale"]');

  document.title = meta.title;
  if (description) description.setAttribute("content", meta.description);
  if (ogTitle) ogTitle.setAttribute("content", meta.ogTitle);
  if (ogDescription) ogDescription.setAttribute("content", meta.ogDescription);
  if (ogLocale) ogLocale.setAttribute("content", meta.ogLocale);
}

function applyTranslations(language) {
  document.documentElement.lang = language;
  updateMetadata(language);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const translation = getTranslation(language, element.dataset.i18n);
    if (translation) element.textContent = translation;
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const translation = getTranslation(language, element.dataset.i18nPlaceholder);
    if (translation) element.setAttribute("placeholder", translation);
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const translation = getTranslation(language, element.dataset.i18nAriaLabel);
    if (translation) element.setAttribute("aria-label", translation);
  });
}

function updateLanguageButtons(language) {
  languageButtons.forEach((button) => {
    const isActive = button.dataset.language === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
}

function setLanguage(language) {
  const nextLanguage = translations[language] ? language : defaultLanguage;
  applyTranslations(nextLanguage);
  updateLanguageButtons(nextLanguage);
  saveLanguage(nextLanguage);
}

function setupLanguageSwitcher() {
  languageButtons.forEach((button) => {
    button.setAttribute("aria-pressed", "false");
    button.addEventListener("click", () => {
      setLanguage(button.dataset.language);
    });
  });

  setLanguage(getStoredLanguage() || defaultLanguage);
}

function setupCurrentYear() {
  if (yearTarget) {
    yearTarget.textContent = new Date().getFullYear();
  }
}

window.addEventListener("scroll", updateHeaderState, { passive: true });
updateHeaderState();
setupNavigation();
setupRevealAnimations();
setupActiveNavigation();
setupLanguageSwitcher();
setupCurrentYear();
